"""

x + y

- Add two operands

"""

# float

a : float = 1.0
b : float = 0.5
print(a + b)
# 1.5

# =============================================================

# integer

a : int = 2
b : int = 3
print(a + b)
# 5

# =============================================================

# list

a : list[int] = [1, 2, 3]
b : list[int] = [4, 5, 6]
print(a + b)
# [1, 2, 3, 4, 5, 6]

# =============================================================

# string

a : str = "hello "
b : str = "world"
print(a + b)
# "hello world"