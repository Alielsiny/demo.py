"""

x - y

- Subtract two operands

"""

# float - float

a : float = 1.0
b : float = 0.5
print(a - b)
# 0.5

# -------------------------------------------------------------

# float - integer

a : float = 1.0
b : int = 1
print(a - b)
# 0.0

# =============================================================

# integer - float

a : int = 2
b : float = 1.0
print(a - b)
# 1.0

# -------------------------------------------------------------

# integer - integer

a : int = 5
b : int = 3
print(a - b)
# 2