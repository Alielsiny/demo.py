"""

x % y

- Return the remainder when the first operand is divided by
    the second

"""

# float % float

print(
  15.2 % 3.0
)
# 0.199...

# -------------------------------------------------------------

# float % integer

print(
  15.2 % 3
)
# 0.199...

# =============================================================

# integer % float

print(
  16 % 3.0
)
# 1.0

# -------------------------------------------------------------

# integer % integer

print(
  17 % 3
)
# 2