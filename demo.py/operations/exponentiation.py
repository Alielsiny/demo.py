"""

x ** y

- Return the first raised to power second

"""

# float ** float

print(
  2.0 ** 2.5
)
# 5.656854249492381

# -------------------------------------------------------------

# float ** integer

print(
  2.0 ** 3
)
# 8.0

# =============================================================

# integer ** float

print(
  2 ** 3.3
)
# 9.849155306759329

# -------------------------------------------------------------

# integer ** integer

print(
  2 ** 4
)
# 16