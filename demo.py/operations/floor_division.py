"""

x // y

- Divide the first operand by the second

"""

# float // float

print(
  4.0 / 2.0
)
# 2

# -------------------------------------------------------------

# float // integer

print(
  4.0 / 2
)
# 2

# =============================================================

# integer // float

print(
  4 // 2.0
)
# 2

# -------------------------------------------------------------

# integer // integer

print(
  4 // 2
)
# 2