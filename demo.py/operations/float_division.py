"""

x / y

- Divide the first operand by the second

"""

# float / float

print(
  5.0 / 2.0
)
# 2.5

# -------------------------------------------------------------

# float / integer

print(
  5.0 / 2
)
# 2.5

# =============================================================

# integer / float

print(
  4 / 2.0
)
# 2.0

# -------------------------------------------------------------

# integer / integer

print(
  4 / 2
)
# 2.0